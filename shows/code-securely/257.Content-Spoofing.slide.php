        <h2>Content Spoofing</h2>

        <p>Content spoofing, also referred to as content injection, is an attack targeting a user
            made possible by an injection vulnerability in a web application.
            When an application does not properly handle user supplied data,
            an attacker can supply content to a web application that is reflected back to the user.
            This presents the user with a modified page under the context of the trusted domain.</p>
