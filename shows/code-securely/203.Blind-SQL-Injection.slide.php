        <h2>Blind SQL Injection</h2>

        <p>Blind SQL injection is a type of SQL Injection attack that asks the database true or false questions
            and determines the answer based on the applications response.
            This attack is often used when the web application is configured to show generic error messages,
            but has not mitigated the code that is vulnerable to SQL injection.</p>
